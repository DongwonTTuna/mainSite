function getMarkdownTitle(markdown: string): string {
  const firstLine = markdown.trim().split(/\r?\n/, 1)[0] ?? "";

  return firstLine.replace(/^#\s+/, "");
}

function parseMarkdownBlocks(markdown: string): BlogArticleBlock[] {
  const [, ...lines] = markdown.trim().split(/\r?\n/);
  const blocks: BlogArticleBlock[] = [];
  let paragraphLines: string[] = [];

  function flushParagraph() {
    if (paragraphLines.length === 0) {
      return;
    }

    blocks.push({
      type: "paragraph",
      text: paragraphLines.join(" "),
    });
    paragraphLines = [];
  }

  for (const line of lines) {
    if (line.startsWith("## ")) {
      flushParagraph();
      blocks.push({
        type: "heading",
        level: 2,
        text: line.slice(3),
      });
      continue;
    }

    if (!line.trim()) {
      flushParagraph();
      continue;
    }

    paragraphLines.push(line.trim());
  }

  flushParagraph();

  return blocks;
}

